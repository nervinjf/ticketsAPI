module.exports = function diaSemana(dia) {
    switch (dia) {
        case 1:
            return "LUNES";
        case 2:
            return "MARTES";
        case 3:
            return "MIERCOLES";
        case 4:
            return "JUEVES";
        case 5:
            return "VIERNES";
    }

}//function buscaEtiquetaStatus
